package malte.entities;

/**
 *
 * @author Malte
 */
public class Row {
    private int fours;
    private int twos;
    private int ones;

    public Row() {
    }

    public int getFours() {
        return fours;
    }

    public void setFours(int fours) {
        this.fours = fours;
    }

    public int getTwos() {
        return twos;
    }

    public void setTwos(int twos) {
        this.twos = twos;
    }

    public int getOnes() {
        return ones;
    }

    public void setOnes(int ones) {
        this.ones = ones;
    }
    
    
}
